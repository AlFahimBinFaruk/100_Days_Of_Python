# slicing
name="suhan"
# only print 2nd index
print(name[2])
# print 0 to 1st index
print(name[0:2])
# by default the first index will be 0
print(name[:2])
# the last index will be the last index of name
print(name[1:])
# there are also negative index,[-4,-1] is same as [1:4]
print("negative",name[-4:-1])
# third arg is the skip value
print(name[1:3:1])
